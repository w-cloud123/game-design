// ============================================================
// src/main.cpp — 程序入口
// ============================================================
// 整个 main 函数只做三件事：
//   1. 构造 Game 对象（这会创建窗口、加载资源、初始化所有状态）
//   2. 调用 game.run() 进入主循环（阻塞，直到窗口关闭）
//   3. 如果抛出异常，打印错误并返回 1
//
// SFML 框架本身不需要额外的初始化/清理步骤，
// Game 对象的析构函数会自动释放所有资源（RAII）。
// ============================================================
#include "Game.hpp"
#include <iostream>

int main() {
    try {
        Game game;
        game.run();
    } catch (const std::exception& e) {
        std::cerr << "Fatal error: " << e.what() << std::endl;
        return 1;
    }
    return 0;
}
